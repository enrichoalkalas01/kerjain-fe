				// Sending ajax request
				ajax.send();
			})

			$('.next').on('click', function() {
				$('#jobs-listing').html(` `);
				ajax.open(method, dataJobs.next_page_url, asynchronous);
	
				// Sending ajax request
				ajax.send();
			})

			$('.link-number').on('click', function() {
				$('.link-number').removeClass('active');
				$(this).addClass('active');
			})
		}
