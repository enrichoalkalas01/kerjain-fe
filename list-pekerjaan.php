	// Call Ajax
	var ajax = new XMLHttpRequest();
	var method = "GET";
	var url = "http://febrianti-laravel.herokuapp.com/jobs-data";
	var asynchronous = true;

	ajax.open(method, url, asynchronous);
	
	// Sending ajax request
	ajax.send();	

	// Receiving response from data.php
	ajax.onreadystatechange = function() {
		if (this.readyState == 4 && this.status == 200) {
			// Converting JSON back to array
			var dataJobs = JSON.parse(this.responseText);
			console.log(dataJobs); // For Debugging

			$('#counted-list-jobs').html(dataJobs.data.length + " Pekerjaan & Lowongan")

			$('#jobs-listing').html(` `);
			for ( let i = 0; i < dataJobs.data.length; i++ ) {
				$('#jobs-listing').append(
					`<div class="job-listing wtabs">
						<div class="job-title-sec">
							<div class="c-logo" style="padding-right: 25px; width: 15%;"> <img src="`+ dataJobs.data[i].logo +`" alt="" /> </div>
							<h3><a href="detail-pekerjaan.php/`+ dataJobs.data[i].id +`" title="">`+ dataJobs.data[i].title +`</a></h3>
							<span>`+ dataJobs.data[i].excerpt +`</span>
							<div class="job-lctn"><i class="la la-map-marker"></i>`+ dataJobs.data[i].location +`</div>
						</div>
						<div class="job-style-bx">
							<span class="job-is ft">Full time</span>
							<span class="fav-job"><i class="la la-heart-o"></i></span>
							<i>5 bulan yang lalu</i>
						</div>
